bool visited[N][N] = {false};
bool solution[N][N] = {false};

bool unvisited_path(int x, int y) {
    return (x >= 0 && x < N && y >= 0 && y < N &&
            labyrinthe[x][y] == 1 &&
            !visited[x][y]);
}

bool find_exit(int x, int y) {
    if (x == N - 1 && y == N - 1) {
        visited[x][y] = true;
        solution[x][y] = true;
        return true;
    }

    if (!unvisited_path(x, y))
        return false;

    visited[x][y] = true;

    int dx[] = {0, 1, 0, -1};
    int dy[] = {1, 0, -1, 0};

    for (int dir = 0; dir < 4; dir++) {
        int nx = x + dx[dir];
        int ny = y + dy[dir];

        if (find_exit(nx, ny)) {
            solution[x][y] = true;
            return true;
        }
    }

    return false; // Backtracking
}

